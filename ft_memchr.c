#include "libft.h"

void *ft_memchr(const void *s, int c, size_t size)
{
	unsigned char *ptr = (unsigned char*)s;

	while (size-- && s != 0)
	{
		if (*ptr == c)
			return (ptr);
		ptr++;
	}
	return (0);
}